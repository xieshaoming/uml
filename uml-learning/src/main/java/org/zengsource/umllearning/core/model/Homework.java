package org.zengsource.umllearning.core.model;

public class Homework {

	private String csId;
	private String tid;
	private String csName;
	private String csHomeworkId;
	private String csScore;
	private boolean csFlag;

	public String getCsId() {
		return csId;
	}

	public void setCsId(String csId) {
		this.csId = csId;
	}

	public String getTid() {
		return tid;
	}

	public void setTid(String tid) {
		this.tid = tid;
	}

	public String getCsName() {
		return csName;
	}

	public void setCsName(String csName) {
		this.csName = csName;
	}

	public String getCsHomeworkId() {
		return csHomeworkId;
	}

	public void setCsHomeworkId(String csHomeworkId) {
		this.csHomeworkId = csHomeworkId;
	}

	public String getCsScore() {
		return csScore;
	}

	public void setCsScore(String csScore) {
		this.csScore = csScore;
	}

	public boolean isCsFlag() {
		return csFlag;
	}

	public void setCsFlag(boolean csFlag) {
		this.csFlag = csFlag;
	}

}
